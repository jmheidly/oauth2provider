package oauth2provider

import (
	"crypto/rsa"
	"errors"
	"fmt"

	"github.com/credli/osin"
	"github.com/dgrijalva/jwt-go"
)

var (
	verifyKey *rsa.PublicKey
	signKey   *rsa.PrivateKey
)

var (
	privKeyPath = privatekey // openssl genrsa -out app.rsa keysize
	pubKeyPath  = publickey  // openssl rsa -in app.rsa -pubout > app.rsa.pub
)

// JWT access token generator
type AccessTokenGenJWT struct {
	PrivateKey []byte
	PublicKey  []byte
}

func (c *AccessTokenGenJWT) GenerateAccessToken(data *osin.AccessData, generaterefresh bool) (accesstoken string, refreshtoken string, err error) {
	// generate JWT access token
	token := jwt.New(jwt.GetSigningMethod("RS256"))
	token.Claims["cid"] = data.Client.GetId()
	token.Claims["exp"] = data.ExpireAt().Unix()
	token.Claims["accesstype"] = "online"

	accesstoken, err = token.SignedString(c.PrivateKey)
	if err != nil {
		return "", "", err
	}

	if !generaterefresh {
		return
	}

	// generate JWT access token
	token = jwt.New(jwt.GetSigningMethod("RS256"))
	token.Claims["cid"] = data.Client.GetId()
	token.Claims["at"] = accesstoken
	token.Claims["exp"] = data.ExpireAt().Unix()

	refreshtoken, err = token.SignedString(c.PrivateKey)
	if err != nil {
		return "", "", err
	}
	return
}

func (c *AccessTokenGenJWT) VerToken(tok string) error {

	verifyBytes := pubKeyPath

	verifyKey, err := jwt.ParseRSAPublicKeyFromPEM(verifyBytes)

	token, err := jwt.Parse(tok, func(token *jwt.Token) (interface{}, error) {
		// since we only use the one private key to sign the tokens,
		// we also only use its public counter part to verify
		return verifyKey, nil
	})

	// branch out into the possible error from signing
	switch err.(type) {
	case nil: // no error

		if !token.Valid { // but may still be invalid
			return errors.New("WHAT? Invalid Token? F*** off!")
		}

		// see stdout and watch for the CustomUserInfo, nicely unmarshalled
		return errors.New(fmt.Sprintf("%v", token.Claims))

	case *jwt.ValidationError: // something was wrong during the validation
		vErr := err.(*jwt.ValidationError)

		switch vErr.Errors {
		case jwt.ValidationErrorExpired:

			return errors.New("Token Expired, get a new one.")

		default:
			return errors.New(vErr.Error())
		}

	default: // something else went wrong
		return errors.New(err.Error())
	}
	return nil
}

